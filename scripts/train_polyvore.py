IMAGE_SIZE = 224
IMAGE_SUFFIXES = (".jpg", ".jpeg", ".png", ".webp")


@dataclass
class PairExample:
    left_path: Path
    right_path: Path
    label: int


@dataclass
class ResolvedItem:
    item_id: str
    path: Path
    category_id: int


class PolyvorePairDataset(Dataset):
    def __init__(self, examples, transform):
        self.examples = examples
        self.transform = transform

    def __len__(self):
        return len(self.examples)

    def __getitem__(self, index):
        example = self.examples[index]
        left_image = load_image(example.left_path)
        right_image = load_image(example.right_path)
        label = torch.tensor(example.label, dtype=torch.float32)
        return self.transform(left_image), self.transform(right_image), label


